function abrir(){
    document.getElementById('backmenu-id').className = 'backmenu-after'
    document.getElementById('menuitens-id').className = 'menuitens-after'
    document.getElementById('menuicon-id').className = 'menuicon-after'
    document.getElementById('xis-id').className = 'xis-after'
}

function fechar(){
    document.getElementById('backmenu-id').className = 'backmenu'
    document.getElementById('menuitens-id').className = 'menuitens'
    document.getElementById('menuicon-id').className = 'menuicon'
    document.getElementById('xis-id').className = 'xis'
}
